import React, { useEffect, useRef } from 'react';

const InteractiveNeuralVortex = () => {
  const canvasRef = useRef<HTMLCanvasElement>(null);
  const pointer = useRef({ x: 0, y: 0, tX: 0, tY: 0 }); // Real-time pointer updates
  const animationRef = useRef<number | null>(null);

  // WebGL setup
  useEffect(() => {
    const canvasEl = canvasRef.current;
    if (!canvasEl) return;

    // Initialize WebGL context
    const gl = canvasEl.getContext('webgl') || canvasEl.getContext('experimental-webgl');
    if (!gl) {
      console.error('WebGL not supported');
      return;
    }
    const webGLContext = gl as WebGLRenderingContext;

    // Shader sources
    const vsSource = `
      precision mediump float;
      attribute vec2 a_position;
      varying vec2 vUv;
      void main() {
        vUv = .5 * (a_position + 1.);
        gl_Position = vec4(a_position, 0.0, 1.0);
      }
    `;

    const fsSource = `
      precision mediump float;
      varying vec2 vUv;
      uniform float u_time;
      uniform float u_ratio;
      uniform vec2 u_pointer_position;
      uniform float u_scroll_progress;

      vec2 rotate(vec2 uv, float th) {
        return mat2(cos(th), sin(th), -sin(th), cos(th)) * uv;
      }

      float neuro_shape(vec2 uv, float t, float p) {
        vec2 sine_acc = vec2(0.);
        vec2 res = vec2(0.);
        float scale = 8.;
        for (int j = 0; j < 15; j++) {
          uv = rotate(uv, 1.);
          sine_acc = rotate(sine_acc, 1.);
          vec2 layer = uv * scale + float(j) + sine_acc - t;
          sine_acc += sin(layer) + 2.4 * p;
          res += (.5 + .5 * cos(layer)) / scale;
          scale *= (1.2);
        }
        return res.x + res.y;
      }

      void main() {
        vec2 uv = .5 * vUv;
        uv.x *= u_ratio;
        vec2 pointer = vUv - u_pointer_position;
        pointer.x *= u_ratio;
        float p = clamp(length(pointer), 0., 1.);
        p = .5 * pow(1. - p, 2.);
        float t = .001 * u_time;
        vec3 color = vec3(0.);
        float noise = neuro_shape(uv, t, p);
        noise = 1.2 * pow(noise, 3.);
        noise += pow(noise, 10.);
        noise = max(.0, noise - .5);
        noise *= (1. - length(vUv - .5));
        color = vec3(0.5, 0.15, 0.65);
        color = mix(color, vec3(0.02, 0.7, 0.9), 0.32 + 0.16 * sin(2.0 * u_scroll_progress + 1.2));
        color += vec3(0.15, 0.0, 0.6) * sin(2.0 * u_scroll_progress + 1.5);
        color = color * noise;
        gl_FragColor = vec4(color, noise);
      }
    `;

    // Shader compilation
    const compileShader = (gl: WebGLRenderingContext, source: string, type: number) => {
      const shader = gl.createShader(type);
      if (!shader) return null;
      gl.shaderSource(shader, source);
      gl.compileShader(shader);
      if (!gl.getShaderParameter(shader, gl.COMPILE_STATUS)) {
        console.error('Shader error:', gl.getShaderInfoLog(shader));
        gl.deleteShader(shader);
        return null;
      }
      return shader;
    };

    const vertexShader = compileShader(webGLContext, vsSource, webGLContext.VERTEX_SHADER);
    const fragmentShader = compileShader(webGLContext, fsSource, webGLContext.FRAGMENT_SHADER);

    // Program setup
    const program = webGLContext.createProgram();
    if (!program) return;
    webGLContext.attachShader(program, vertexShader!);
    webGLContext.attachShader(program, fragmentShader!);
    webGLContext.linkProgram(program);
    if (!webGLContext.getProgramParameter(program, webGLContext.LINK_STATUS)) {
      console.error('Program link error:', webGLContext.getProgramInfoLog(program));
      return;
    }
    webGLContext.useProgram(program);

    // Geometry
    const vertices = new Float32Array([-1, -1, 1, -1, -1, 1, 1, 1]);
    const vertexBuffer = webGLContext.createBuffer();
    webGLContext.bindBuffer(webGLContext.ARRAY_BUFFER, vertexBuffer);
    webGLContext.bufferData(webGLContext.ARRAY_BUFFER, vertices, webGLContext.STATIC_DRAW);

    const positionLocation = webGLContext.getAttribLocation(program, 'a_position');
    webGLContext.enableVertexAttribArray(positionLocation);
    webGLContext.bindBuffer(webGLContext.ARRAY_BUFFER, vertexBuffer);
    webGLContext.vertexAttribPointer(positionLocation, 2, webGLContext.FLOAT, false, 0, 0);

    // Uniforms
    const uTime = webGLContext.getUniformLocation(program, 'u_time');
    const uRatio = webGLContext.getUniformLocation(program, 'u_ratio');
    const uPointerPosition = webGLContext.getUniformLocation(program, 'u_pointer_position');
    const uScrollProgress = webGLContext.getUniformLocation(program, 'u_scroll_progress');

    // Resize handler
    const resizeCanvas = () => {
      const devicePixelRatio = Math.min(window.devicePixelRatio, 2);
      canvasEl.width = window.innerWidth * devicePixelRatio;
      canvasEl.height = window.innerHeight * devicePixelRatio;
      webGLContext.viewport(0, 0, canvasEl.width, canvasEl.height);
      webGLContext.uniform1f(uRatio, canvasEl.width / canvasEl.height);
    };

    resizeCanvas();
    window.addEventListener('resize', resizeCanvas);

    // Animation loop
    const render = () => {
      const currentTime = performance.now();

      // Smooth pointer movement
      pointer.current.x += (pointer.current.tX - pointer.current.x) * 0.2;
      pointer.current.y += (pointer.current.tY - pointer.current.y) * 0.2;

      webGLContext.uniform1f(uTime, currentTime);
      webGLContext.uniform2f(uPointerPosition,
        pointer.current.x / window.innerWidth,
        1 - pointer.current.y / window.innerHeight
      );
      webGLContext.uniform1f(uScrollProgress, window.pageYOffset / (2 * window.innerHeight));

      webGLContext.drawArrays(webGLContext.TRIANGLE_STRIP, 0, 4);
      animationRef.current = requestAnimationFrame(render);
    };

    render();

    // Event listeners
    const handleMouseMove = (e: PointerEvent) => {
      pointer.current.tX = e.clientX;
      pointer.current.tY = e.clientY;
    };

    window.addEventListener('pointermove', handleMouseMove);
    window.addEventListener('touchmove', (e) => {
      if (e.touches[0]) {
        pointer.current.tX = e.touches[0].clientX;
        pointer.current.tY = e.touches[0].clientY;
      }
    });

    // Cleanup
    return () => {
      window.removeEventListener('resize', resizeCanvas);
      window.removeEventListener('pointermove', handleMouseMove);
      if (animationRef.current !== null) {
        cancelAnimationFrame(animationRef.current);
      }
      webGLContext.deleteProgram(program);
      if (vertexShader) webGLContext.deleteShader(vertexShader);
      if (fragmentShader) webGLContext.deleteShader(fragmentShader);
    };
  }, []);

  return (
    <div className="absolute inset-0 w-full h-full pointer-events-none opacity-60 z-0">
      <canvas
        ref={canvasRef}
        id="neuro"
        className="fixed inset-0 w-full h-full pointer-events-none"
      ></canvas>
    </div>
  );
};

export default InteractiveNeuralVortex;
